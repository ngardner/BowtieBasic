	    </div>
	    <div class="panel-body">
		<p>Bowtie Basic comes with just the fundamental's, to keep your projects lightweight and fast.</p>
		<p>Bowtie uses the MVC design pattern, and allows for multiple interfaces - each with their own controllers and views, but sharing the same models.</p>
		<p>Bowtie Basic also comes with controller/action routing, and a MySQL database library.</p>
		<p>Everything else is left up to you. If you are looking for a more complete framework, check back soon for information about <i>Bowtie CMS</i></p>
